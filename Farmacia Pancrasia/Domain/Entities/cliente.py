class Cliente:
    def __init__(self, id : int, nombre : str, apellido : str, telefono : str, puntos : int, direccion : str, email : str):
        self.id = id
        self.nombre = nombre
        self.apellido = apellido
        self.telefono = telefono
        self.puntos = puntos
        self.direccion = direccion
        self.email = email